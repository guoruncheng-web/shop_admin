// ÂàáÊç¢ËèúÂçïÁä∂ÊÄÅ
const handleStatusChange = async (menu: MenuPermission, newStatus: boolean) => {
  // Â¶ÇÊûúÊ≠£Âú®ÂàùÂßãÂåñÔºå‰∏çÊâßË°åÁä∂ÊÄÅÂàáÊç¢
  if (isInitializing.value) {
    return;
  }
  
  const originalStatus = menu.status;
  
  try {
