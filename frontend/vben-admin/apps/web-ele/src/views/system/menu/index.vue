// ËèúÂçïÊ†ëÈÄâÈ°πÔºàÁî®‰∫éË°®Âçï‰∏≠ÁöÑ‰∏äÁ∫ßËèúÂçïÈÄâÊã©Ôºâ
const menuTreeOptions = computed(() => {
  const buildTreeOptions = (menus: MenuPermission[]): MenuPermission[] => {
    return menus
      .filter(menu => menu.type !== 3) // ÊåâÈíÆ‰∏çËÉΩ‰Ωú‰∏∫Áà∂ËèúÂçï
      .map(menu => ({
        ...menu,
        children: menu.children ? buildTreeOptions(menu.children) : undefined,
      }));
  };
  
  // Ê∑ªÂä†Ê†πËäÇÁÇπÈÄâÈ°π
  return [
    { id: 0, name: 'Ê†πÁõÆÂΩï', type: 1, status: true, sort_order: 0, orderNum: 0 } as MenuPermission,
    ...buildTreeOptions(menuTreeData.value),
  ];
});

const loadMenuList = async () => {
  try {
    loading.value = true;
    console.log('üîç Êü•ËØ¢ËèúÂçïÊ†ëÂèÇÊï∞:', searchForm);
    
    const response = await getMenuTreeApi(searchForm);
    console.log('üìã ËèúÂçïÊ†ëÂìçÂ∫î:', response);

    if (response) {
      // Â§ÑÁêÜ‰∏çÂêåÁöÑÂìçÂ∫îÊ†ºÂºè
      let menuData: MenuPermission[] = [];
      
      if (Array.isArray(response)) {
        menuData = response;
      } else if (response.data && Array.isArray(response.data)) {
        menuData = response.data;
      } else if (response.code === 200 && response.data && Array.isArray(response.data)) {
        menuData = response.data;
      }
      
      // Á°Æ‰øùÊØè‰∏™ËèúÂçïÈ°πÈÉΩÊúâÂøÖË¶ÅÁöÑÂ±ûÊÄßÔºåÂπ∂ÁîüÊàêÂêàÈÄÇÁöÑÊòæÁ§∫ÂêçÁß∞
      const processMenuData = (menus: any[]): MenuPermission[] => {
        return menus.map(menu => {
          // ÁîüÊàêÊòæÁ§∫ÂêçÁß∞ - ‰ºòÂÖà‰ΩøÁî®ÈùûÁ©∫ÁöÑ name Êàñ titleÔºåÂê¶ÂàôÊ†πÊçÆË∑ØÂæÑÂíåÁ±ªÂûãÁîüÊàê
